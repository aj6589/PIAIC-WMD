"use strict";
function check_number() {
    let n;
    n = -8;
    if (n == 0) {
        console.log("The Given number is Zero");
    }
    else if (n >= 0) {
        console.log("The Given number is positive ");
    }
    else {
        console.log("The Given number is Negative");
    }
}
check_number();
